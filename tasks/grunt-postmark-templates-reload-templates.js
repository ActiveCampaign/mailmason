/*
 * grunt-postmark-templates-generate
 * Generate templates to push to a Postmark server for use with grunt-postmark
 *
 * https://github.com/wildbit/grunt-postmark.git
 */

module.exports = function (grunt) {
  grunt.registerTask('postmark-templates-reload-templates', 'Reload template configuration generated by `postmark-templates-generate` for `postmark-templates-upload`', function () {
    grunt.config.set(
      'postmark-templates-upload',
      grunt.config.get('config.templates.output_file')
        ? grunt.file.readJSON(grunt.config.get('config.templates.output_file'))
        : grunt.file.readJSON(grunt.config.get('config.templates.file'))
    );
  });
};
